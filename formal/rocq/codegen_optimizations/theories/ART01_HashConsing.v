(*
 * ART01_HashConsing: Interning preserves alpha-equivalence and fixpoint
 * semantics for recursive term types.
 *
 * Hash-consing replaces Box<T> with Rc<T> backed by an interning table,
 * guaranteeing that structurally identical sub-terms share a single heap
 * allocation.  This file proves:
 *   1. Interning preserves alpha-equivalence
 *   2. Interning preserves fixpoint semantics
 *   3. Interning is idempotent (within a single epoch)
 *   4. Interned equality iff alpha-equivalence (hash-consing correctness)
 *
 * The Rust implementation uses epoch-based cache clearing
 * (hash_consing.rs:55-61): the intern table is cleared at the start of
 * each Ascent fixpoint evaluation.  All properties are parameterized
 * over an epoch to reflect this — idempotency and canonical interning
 * hold within a single epoch, but NOT across epoch boundaries.
 *
 * Spec-to-Code Traceability:
 *   Rocq Definition              | Rust Code                                | Location
 *   -----------------------------|------------------------------------------|-----------------------------------------
 *   Term (inductive)             | enum NonTerminal                         | macros/src/gen/runtime/language.rs
 *   Epoch                        | INTERN_EPOCH thread-local counter        | runtime/src/hash_consing.rs:55
 *   intern                       | hash_consing table lookup                | runtime/src/hash_consing.rs
 *   alpha_eq                     | PartialEq for NonTerminal                | macros/src/gen/runtime/language.rs
 *   fixpoint F(S)                | Ascent fixpoint evaluation               | macros/src/logic/common.rs
 *   Optimization::HashConsing    | cost_benefit::Optimization::HashConsing   | prattail/src/cost_benefit.rs:76
 *   OptimizationGates::hash_consing | hash_consing gate field               | prattail/src/cost_benefit.rs:1150
 *
 * Rocq 9.1 compatible.
 *)

