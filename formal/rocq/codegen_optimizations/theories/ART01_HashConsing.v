(* ===================================================================== *)
(*  Term Inductive Type                                                    *)
(*                                                                         *)
(*  Models the recursive NonTerminal type generated by                     *)
(*  macros/src/gen/runtime/language.rs.  Constructors carry a tag (nat)    *)
(*  and a list of sub-terms, modeling arbitrary arity constructors.        *)
(* ===================================================================== *)

Inductive Term : Type :=
  | TVar : nat -> Term               (* variable reference by de Bruijn index *)
  | TCon : nat -> list Term -> Term.  (* constructor tag + sub-terms *)

(* ===================================================================== *)
(*  Strong Induction Principle for Nested Inductives                       *)
(*                                                                         *)
(*  Rocq 9.1 does not generate mutual induction principles for nested     *)
(*  inductives using list.  We define one manually.                        *)
(* ===================================================================== *)

Fixpoint Term_ind_nested (P : Term -> Prop) (Pl : list Term -> Prop)
  (HVar : forall n, P (TVar n))
  (HCon : forall tag args, Pl args -> P (TCon tag args))
  (HNil : Pl nil)
  (HCons : forall t rest, P t -> Pl rest -> Pl (t :: rest))
  (t : Term) : P t :=
  match t with
  | TVar n => HVar n
  | TCon tag args =>
      HCon tag args
        ((fix go (l : list Term) : Pl l :=
          match l with
          | nil => HNil
          | cons x xs => HCons x xs (Term_ind_nested P Pl HVar HCon HNil HCons x) (go xs)
          end) args)
  end.

(* ===================================================================== *)
(*  Alpha-Equivalence                                                      *)
(*                                                                         *)
(*  Structural equality up to variable identity.  Since we use de Bruijn   *)
(*  indices, alpha-equivalence coincides with structural equality.         *)
(*                                                                         *)
(*  Uses inner fix for the list case to satisfy Rocq 9.1's guard checker  *)
(*  (mutual Fixpoint over nested inductives is not accepted).              *)
(* ===================================================================== *)

Fixpoint term_eq_dec (t1 t2 : Term) : bool :=
  match t1, t2 with
  | TVar n1, TVar n2 => Nat.eqb n1 n2
  | TCon tag1 args1, TCon tag2 args2 =>
      Nat.eqb tag1 tag2 &&
      (fix list_eq (l1 l2 : list Term) : bool :=
        match l1, l2 with
        | nil, nil => true
        | cons x1 r1, cons x2 r2 => term_eq_dec x1 x2 && list_eq r1 r2
        | _, _ => false
        end) args1 args2
  | _, _ => false
  end.

(* Top-level wrapper for the list equality function *)
Definition list_term_eq_dec (l1 l2 : list Term) : bool :=
  (fix go (l1 l2 : list Term) : bool :=
    match l1, l2 with
    | nil, nil => true
    | cons x1 r1, cons x2 r2 => term_eq_dec x1 x2 && go r1 r2
    | _, _ => false
    end) l1 l2.

(* Compatibility: term_eq_dec on TCon unfolds to use list_term_eq_dec *)
Lemma term_eq_dec_TCon : forall tag1 args1 tag2 args2,
  term_eq_dec (TCon tag1 args1) (TCon tag2 args2) =
  Nat.eqb tag1 tag2 && list_term_eq_dec args1 args2.
Proof. intros. simpl. unfold list_term_eq_dec. reflexivity. Qed.

Definition alpha_eq (t1 t2 : Term) : Prop := term_eq_dec t1 t2 = true.

(* ===================================================================== *)
(*  Helper Lemmas                                                          *)
(* ===================================================================== *)

Lemma alpha_eq_refl : forall t, alpha_eq t t.
Proof.
  intro t.
  apply (Term_ind_nested
    (fun t => alpha_eq t t)
    (fun l => list_term_eq_dec l l = true)); clear t.
  - intro n. unfold alpha_eq. simpl. rewrite Nat.eqb_refl. reflexivity.
  - intros tag args IH. unfold alpha_eq.
    rewrite term_eq_dec_TCon. rewrite Nat.eqb_refl. simpl. exact IH.
  - simpl. reflexivity.
  - intros t rest IHt IHrest. simpl. rewrite IHt. simpl. exact IHrest.
Qed.

Lemma alpha_eq_sym : forall t1 t2, alpha_eq t1 t2 -> alpha_eq t2 t1.
Proof.
  unfold alpha_eq.
  intro t1.
  apply (Term_ind_nested
    (fun t1 => forall t2, term_eq_dec t1 t2 = true -> term_eq_dec t2 t1 = true)
    (fun l1 => forall l2, list_term_eq_dec l1 l2 = true -> list_term_eq_dec l2 l1 = true)
  ); clear t1.
  - intros n t2 H. destruct t2; simpl in *; try discriminate.
    rewrite Nat.eqb_sym. exact H.
  - intros tag args IHargs t2 H.
    destruct t2 as [n | n l]; [simpl in H; discriminate |].
    rewrite term_eq_dec_TCon in H. rewrite term_eq_dec_TCon.
    apply Bool.andb_true_iff in H as [Htag Hargs].
    rewrite Nat.eqb_sym. rewrite Htag. simpl.
    apply IHargs. exact Hargs.
  - intros l2 H. destruct l2; simpl in *; try discriminate. reflexivity.
  - intros t1 rest IHt1 IHrest l2 H.
    destruct l2; simpl in *; try discriminate.
    apply Bool.andb_true_iff in H as [Ht Hr].
    rewrite (IHt1 _ Ht). simpl. apply IHrest. exact Hr.
Qed.

Lemma alpha_eq_trans : forall t1 t2 t3,
  alpha_eq t1 t2 -> alpha_eq t2 t3 -> alpha_eq t1 t3.
Proof.
  unfold alpha_eq.
  intro t1.
  apply (Term_ind_nested
    (fun t1 => forall t2 t3, term_eq_dec t1 t2 = true ->
                              term_eq_dec t2 t3 = true ->
                              term_eq_dec t1 t3 = true)
    (fun l1 => forall l2 l3, list_term_eq_dec l1 l2 = true ->
                              list_term_eq_dec l2 l3 = true ->
                              list_term_eq_dec l1 l3 = true)
  ); clear t1.
  - intros n t2 t3 H12 H23.
    destruct t2; simpl in H12; try discriminate.
    destruct t3; simpl in H23; try discriminate.
    simpl. apply Nat.eqb_eq in H12. apply Nat.eqb_eq in H23.
    apply Nat.eqb_eq. lia.
  - intros tag args IHargs t2 t3 H12 H23.
    destruct t2 as [| n2 l2]; [simpl in H12; discriminate |].
    destruct t3 as [| n3 l3]; [simpl in H23; discriminate |].
    rewrite term_eq_dec_TCon in *.
    apply Bool.andb_true_iff in H12 as [Htag12 Hargs12].
    apply Bool.andb_true_iff in H23 as [Htag23 Hargs23].
    apply Nat.eqb_eq in Htag12. apply Nat.eqb_eq in Htag23. subst.
    rewrite Nat.eqb_refl. simpl.
    apply (IHargs _ _ Hargs12 Hargs23).
  - intros l2 l3 H12 H23.
    destruct l2; simpl in H12; try discriminate.
    destruct l3; simpl in H23; try discriminate.
    simpl. reflexivity.
  - intros t1 rest IHt1 IHrest l2 l3 H12 H23.
    destruct l2; simpl in H12; try discriminate.
    destruct l3; simpl in H23; try discriminate.
    apply Bool.andb_true_iff in H12 as [Ht12 Hr12].
    apply Bool.andb_true_iff in H23 as [Ht23 Hr23].
    simpl. rewrite (IHt1 _ _ Ht12 Ht23). simpl.
    apply (IHrest _ _ Hr12 Hr23).
Qed.

(* alpha_eq from Leibniz equality *)
Lemma eq_implies_alpha_eq : forall t1 t2,
  t1 = t2 -> alpha_eq t1 t2.
Proof.
  intros t1 t2 H. subst. apply alpha_eq_refl.
Qed.

(* ===================================================================== *)
(*  Epoch Type                                                             *)
(*                                                                         *)
(*  The Rust implementation uses epoch-based cache clearing: the intern   *)
(*  table is cleared at the start of each Ascent fixpoint evaluation      *)
(*  (hash_consing.rs:55-61).  We parameterize intern over epochs to      *)
(*  reflect that idempotency and canonical form hold only within a        *)
(*  single epoch.                                                          *)
(* ===================================================================== *)

Definition Epoch := nat.

(* ===================================================================== *)
(*  Intern Function (Axiomatized)                                          *)
(*                                                                         *)
(*  The intern function models the hash-consing table: given an epoch and *)
(*  a term, return the canonical representative.  We axiomatize its key   *)
(*  properties rather than implementing the hash table, since the Rust    *)
(*  implementation uses Rc + HashMap which has no direct Rocq analogue.   *)
(*                                                                         *)
(*  All properties are parameterized over a single epoch.  Cross-epoch    *)
(*  guarantees are weaker: interning the same term in different epochs    *)
(*  produces alpha-equivalent (but not necessarily Leibniz-equal) results *)
(*  because the table is cleared between epochs.                           *)
(* ===================================================================== *)

Section Interning.

Variable intern : Epoch -> Term -> Term.

(* Axiom I1: intern maps alpha-equivalent terms to equal results (within an epoch) *)
Hypothesis intern_canonical : forall e t1 t2,
  alpha_eq t1 t2 -> intern e t1 = intern e t2.

(* Axiom I2: intern preserves the structure (result is alpha-equivalent to input) *)
Hypothesis intern_preserves : forall e t,
  alpha_eq (intern e t) t.

(* Axiom I3: intern is idempotent within a single epoch *)
Hypothesis intern_idempotent_ax : forall e t,
  intern e (intern e t) = intern e t.

(* ===================================================================== *)
(*  Theorem 1: intern preserves alpha-equivalence                         *)
(*                                                                         *)
(*  If two terms are alpha-equivalent, their interned forms are also       *)
(*  alpha-equivalent.  This guarantees that hash-consing does not          *)
(*  collapse terms that should be distinguished or distinguish terms       *)
(*  that should be collapsed.                                              *)
(* ===================================================================== *)

Theorem intern_preserves_alpha_eq : forall e t1 t2,
  alpha_eq t1 t2 -> alpha_eq (intern e t1) (intern e t2).
Proof.
  intros e t1 t2 Halpha.
  (* By intern_canonical, alpha_eq t1 t2 implies intern e t1 = intern e t2 *)
  assert (Heq : intern e t1 = intern e t2) by (apply intern_canonical; exact Halpha).
  (* Leibniz equality implies alpha_eq *)
  apply eq_implies_alpha_eq. exact Heq.
Qed.

(* ===================================================================== *)
(*  Cross-Epoch Property                                                   *)
(*                                                                         *)
(*  Interning the same term in different epochs produces alpha-equivalent *)
(*  results (both are canonical forms of the same term), even though the  *)
(*  table was cleared between epochs.  This follows from intern_preserves *)
(*  and alpha_eq transitivity.                                             *)
(* ===================================================================== *)

Lemma cross_epoch_alpha_eq : forall e1 e2 t,
  alpha_eq (intern e1 t) (intern e2 t).
Proof.
  intros e1 e2 t.
  apply (alpha_eq_trans _ t _).
  - apply intern_preserves.
  - apply alpha_eq_sym. apply intern_preserves.
Qed.

(* NOTE: Cross-epoch Leibniz equality (intern e1 t = intern e2 t) is NOT
   guaranteed.  The Rust implementation clears and rebuilds the intern
   table at each epoch boundary, so different Rc pointers may be
   allocated for structurally identical terms in different epochs. *)

(* ===================================================================== *)
(*  Fixpoint Semantics                                                     *)
(*                                                                         *)
(*  We model the Ascent fixpoint as an operator F on sets of terms.        *)
(*  A set is represented as Term -> Prop (characteristic function).        *)
(*  All fixpoint operations occur within a single epoch.                   *)
(* ===================================================================== *)

Definition TermSet := Term -> Prop.

(* Pointwise inclusion *)
Definition ts_subset (S1 S2 : TermSet) : Prop :=
  forall t, S1 t -> S2 t.

(* Pointwise equality *)
Definition ts_eq (S1 S2 : TermSet) : Prop :=
  forall t, S1 t <-> S2 t.

(* Interned set: the image of a set under intern at epoch e *)
Definition interned_set (e : Epoch) (S : TermSet) : TermSet :=
  fun t => exists t', S t' /\ t = intern e t'.

(* Alpha-closure: a set closed under alpha-equivalence *)
Definition alpha_closed (S : TermSet) : Prop :=
  forall t1 t2, S t1 -> alpha_eq t1 t2 -> S t2.

(* Monotone operator *)
Definition monotone (F : TermSet -> TermSet) : Prop :=
  forall S1 S2, ts_subset S1 S2 -> ts_subset (F S1) (F S2).

(* Alpha-compatible operator: F commutes with alpha-equivalence *)
Definition alpha_compatible (F : TermSet -> TermSet) : Prop :=
  forall S t1 t2, alpha_eq t1 t2 -> F S t1 -> F S t2.

(* Intern-compatible operator: F commutes with interning at epoch e *)
Definition intern_compatible (e : Epoch) (F : TermSet -> TermSet) : Prop :=
  forall S t, F S t -> F (interned_set e S) (intern e t).

(* ===================================================================== *)
(*  Theorem 2: intern preserves fixpoint semantics                         *)
(*                                                                         *)
(*  For an alpha-compatible, intern-compatible operator F, the fixpoint    *)
(*  over interned terms equals the interned fixpoint over original terms.  *)
(*  I.e., interning the fixpoint = fixpoint of interned operator.          *)
(*  All operations are within a single epoch e.                            *)
(* ===================================================================== *)

Section FixpointPreservation.

  Variable e : Epoch.
  Variable F : TermSet -> TermSet.
  Hypothesis F_mono : monotone F.
  Hypothesis F_alpha : alpha_compatible F.
  Hypothesis F_intern : intern_compatible e F.

  (* The interned operator: apply F, then intern results *)
  Definition F_interned (S : TermSet) : TermSet :=
    fun t => exists t', F S t' /\ t = intern e t'.

  (* Lemma: if t is in F(S), then intern(t) is in F_interned(S) *)
  Lemma f_to_f_interned : forall S t,
    F S t -> F_interned S (intern e t).
  Proof.
    intros S t HF. unfold F_interned. exists t. split; [exact HF | reflexivity].
  Qed.

  (* Lemma: if t is in F_interned(S), then there exists t' in F(S)
     with t = intern t' *)
  Lemma f_interned_witness : forall S t,
    F_interned S t -> exists t', F S t' /\ t = intern e t'.
  Proof.
    intros S t HFi. unfold F_interned in HFi. exact HFi.
  Qed.

  (* Main theorem: the interned image of F(S) equals F_interned(S) *)
  Theorem intern_preserves_fixpoint : forall S,
    ts_eq (interned_set e (F S)) (F_interned S).
  Proof.
    intros S t. split.
    - (* interned_set e (F S) t -> F_interned S t *)
      intros [t' [HF Heq]].
      unfold F_interned. exists t'. split; assumption.
    - (* F_interned S t -> interned_set e (F S) t *)
      intros [t' [HF Heq]].
      unfold interned_set. exists t'. split; assumption.
  Qed.

End FixpointPreservation.

(* ===================================================================== *)
(*  Theorem 3: intern is idempotent (within a single epoch)                *)
(*                                                                         *)
(*  intern e (intern e t) = intern e t                                     *)
(*  This ensures that re-interning an already interned term is a no-op,   *)
(*  which is critical for the runtime performance guarantee: once a term  *)
(*  passes through the interning table, subsequent lookups are O(1).      *)
(*                                                                         *)
(*  Note: this holds ONLY within a single epoch.  Across epochs, the      *)
(*  table is cleared, so intern e1 (intern e2 t) may differ from          *)
(*  intern e2 t (though they remain alpha-equivalent by                   *)
(*  cross_epoch_alpha_eq).                                                 *)
(* ===================================================================== *)

Theorem intern_idempotent : forall e t,
  intern e (intern e t) = intern e t.
Proof.
  exact intern_idempotent_ax.
Qed.

(* ===================================================================== *)
(*  Theorem 4: Hash-consing correctness (within a single epoch)            *)
(*                                                                         *)
(*  intern e t1 = intern e t2  <->  alpha_eq t1 t2                        *)
(*                                                                         *)
(*  This is the fundamental property of hash-consing: pointer equality     *)
(*  of interned terms is equivalent to structural (alpha) equivalence.    *)
(*  The forward direction (alpha_eq -> equal intern) follows from          *)
(*  intern_canonical.  The reverse direction (equal intern -> alpha_eq)    *)
(*  follows from intern_preserves and alpha_eq transitivity.              *)
(* ===================================================================== *)

Theorem intern_eq_iff_alpha_eq : forall e t1 t2,
  intern e t1 = intern e t2 <-> alpha_eq t1 t2.
Proof.
  intros e t1 t2. split.
  - (* intern e t1 = intern e t2 -> alpha_eq t1 t2 *)
    intro Heq.
    (* alpha_eq t1 (intern e t1) by intern_preserves + symmetry *)
    assert (H1 : alpha_eq t1 (intern e t1)).
    { apply alpha_eq_sym. apply intern_preserves. }
    (* alpha_eq (intern e t2) t2 by intern_preserves *)
    assert (H2 : alpha_eq (intern e t2) t2).
    { apply intern_preserves. }
    (* alpha_eq (intern e t1) (intern e t2) by Leibniz eq *)
    assert (H3 : alpha_eq (intern e t1) (intern e t2)).
    { apply eq_implies_alpha_eq. exact Heq. }
    (* Chain: t1 ~= intern e t1 = intern e t2 ~= t2 *)
    apply (alpha_eq_trans _ (intern e t1) _).
    + exact H1.
    + apply (alpha_eq_trans _ (intern e t2) _).
      * exact H3.
      * exact H2.
  - (* alpha_eq t1 t2 -> intern e t1 = intern e t2 *)
    intro Halpha. apply intern_canonical. exact Halpha.
Qed.

(* ===================================================================== *)
(*  Corollary: interned equality is decidable                              *)
(*                                                                         *)
(*  Since intern returns canonical representatives, equality of interned  *)
(*  terms reduces to pointer comparison, which is O(1) in the Rust        *)
(*  implementation (Rc::ptr_eq or Arc::ptr_eq).                           *)
(* ===================================================================== *)

Corollary intern_decidable_eq : forall e t1 t2,
  {intern e t1 = intern e t2} + {intern e t1 <> intern e t2} ->
  {alpha_eq t1 t2} + {~ alpha_eq t1 t2}.
Proof.
  intros e t1 t2 [Heq | Hneq].
  - left. exact (proj1 (intern_eq_iff_alpha_eq e t1 t2) Heq).
  - right. intro Halpha. apply Hneq.
    exact (proj2 (intern_eq_iff_alpha_eq e t1 t2) Halpha).
Qed.

End Interning.

(* ===================================================================== *)
(*  Abstraction Gaps                                                       *)
(*                                                                         *)
(*  1. Hash collision handling: The Rust uses HashMap with bucket search   *)
(*     to handle hash collisions.  This is sound (always finds the entry  *)
(*     if present) and abstracted away in the Rocq model.                  *)
(*  2. Rc wrapping: The Rust wraps interned terms in Rc<T> for shared     *)
(*     ownership.  Rc is transparent to the Rocq model (type equality     *)
(*     ignores the pointer wrapper).                                       *)
(*  3. Hash+Eq+Clone trait bounds: The Rust requires these traits for     *)
(*     hash-consable types.  In the Rocq model, decidable equality is     *)
(*     implicit (we use alpha_eq which is decidable by term_eq_dec).      *)
(*  4. Thread locality: The Rust uses thread-local intern tables.  The    *)
(*     Rocq model does not distinguish threads; all properties hold per   *)
(*     (epoch, term) pair regardless of thread.                            *)
(*  5. Epoch advancement: The Rust increments INTERN_EPOCH at the start   *)
(*     of each Ascent evaluation.  The Rocq model takes epochs as given   *)
(*     parameters without modeling the advancement mechanism.              *)
(* ===================================================================== *)

