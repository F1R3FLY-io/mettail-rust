#[cfg(test)]
mod tests {
    use super::*;
    use crate::provenance::ProvenanceVar;

    #[test]
    fn test_proof_output_verified() {
        let prov = ProvenanceWeight::var(ProvenanceVar::new("axiom1"));
        let mut proof = ProofOutput::new("safety property P", Verdict::Verified, prov);
        proof.add_step(
            "P is unreachable",
            "prestar analysis",
            vec![ProvenanceVar::new("axiom1")],
        );
        let output = proof.to_human_readable();
        assert!(output.contains("VERIFIED"));
        assert!(output.contains("safety property P"));
        assert!(output.contains("prestar analysis"));
    }

    #[test]
    fn test_proof_output_violated() {
        let prov = ProvenanceWeight::zero();
        let proof = ProofOutput::new("liveness property L", Verdict::Violated, prov);
        let output = proof.to_human_readable();
        assert!(output.contains("VIOLATED"));
    }

    #[test]
    fn test_rocq_certificate_generation() {
        let prov = ProvenanceWeight::one();
        let mut proof = ProofOutput::new("test property", Verdict::Verified, prov);
        proof.add_step("initial state is safe", "assumption", vec![]);
        proof.generate_rocq_certificate();
        let cert = proof.rocq_certificate.as_ref().expect("certificate should be generated");
        assert!(cert.contains("Theorem test_property"));
        assert!(cert.contains("trivial"));
        assert!(cert.contains("Step 1"));
    }

    #[test]
    fn test_proof_step_display() {
        let step = ProofStep {
            step: 1,
            statement: "x = 5".to_string(),
            justification: "assignment".to_string(),
            base_facts: vec![ProvenanceVar::new("fact_a")],
        };
        let s = step.to_string();
        assert!(s.contains("x = 5"));
        assert!(s.contains("assignment"));
        assert!(s.contains("fact_a"));
    }

    #[test]
    fn test_proof_output_display() {
        let prov = ProvenanceWeight::var(ProvenanceVar::new("ax1"))
            .plus(&ProvenanceWeight::var(ProvenanceVar::new("ax2")));
        let proof = ProofOutput::new("multi-axiom property", Verdict::Verified, prov);
        let display = format!("{}", proof);
        assert!(display.contains("multi-axiom property"));
        assert!(display.contains("VERIFIED"));
    }

    #[test]
    fn test_generate_certificates_with_safety() {
        use crate::automata::semiring::BooleanWeight;
        let safety_result = crate::verify::SafetyResult {
            safe: true,
            initial_weight: BooleanWeight(false),
            witness_trace: Vec::new(),
        };
        let certs = generate_certificates(
            #[cfg(feature = "trs-analysis")]
            None,
            #[cfg(feature = "trs-analysis")]
            None,
            Some(&safety_result),
        );
        assert!(!certs.is_empty(), "should produce at least one certificate from safety result");
    }

    #[test]
    fn test_generate_certificates_all_none() {
        let certs = generate_certificates(
            #[cfg(feature = "trs-analysis")]
            None,
            #[cfg(feature = "trs-analysis")]
            None,
            None,
        );
        assert!(certs.is_empty(), "should produce empty vec when all inputs are None");
    }
}
