//! Layered proof output: verdicts, human-readable explanations, Rocq certificates.
//!
//! Every analysis produces a structured result with multiple layers:
//! 1. **Verdict** (always included): verified/violated/unknown + witness traces
//! 2. **Human-readable explanation** (with `proofs` feature): detailed reasoning
//! 3. **Machine-checkable certificate** (with `proofs` feature): Rocq/Coq output
//!
//! The proof output layer consumes provenance information from the provenance
//! semiring to construct explanations that show not just WHAT was proven, but
//! HOW and WHY (which base facts, which derivation paths).

use crate::provenance::{ProvenanceVar, ProvenanceWeight};
use crate::verify::Verdict;
use std::fmt;

/// A proof step in a human-readable proof trace.
#[derive(Debug, Clone)]
pub struct ProofStep {
    /// Step number (1-based).
    pub step: usize,
    /// The statement being justified.
    pub statement: String,
    /// Justification (axiom, inference rule, etc.).
    pub justification: String,
    /// Provenance variables used in this step.
    pub base_facts: Vec<ProvenanceVar>,
}

impl fmt::Display for ProofStep {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "  {}. {} [{}]", self.step, self.statement, self.justification)?;
        if !self.base_facts.is_empty() {
            write!(f, "  (uses: ")?;
            let mut first = true;
            for var in &self.base_facts {
                if !first {
                    write!(f, ", ")?;
                }
                first = false;
                write!(f, "{}", var)?;
            }
            write!(f, ")")?;
        }
        Ok(())
    }
}

/// A complete proof output with layered detail levels.
#[derive(Debug, Clone)]
pub struct ProofOutput {
    /// The property being verified.
    pub property: String,
    /// The verdict.
    pub verdict: Verdict,
    /// Provenance polynomial summarizing how the result was derived.
    pub provenance: ProvenanceWeight,
    /// Human-readable proof steps.
    pub proof_steps: Vec<ProofStep>,
    /// Rocq/Coq certificate source (if generated).
    pub rocq_certificate: Option<String>,
}

impl ProofOutput {
    /// Create a new proof output with a verdict and provenance.
    pub fn new(
        property: impl Into<String>,
        verdict: Verdict,
        provenance: ProvenanceWeight,
    ) -> Self {
        ProofOutput {
            property: property.into(),
            verdict,
            provenance,
            proof_steps: Vec::new(),
            rocq_certificate: None,
        }
    }

    /// Add a proof step.
    pub fn add_step(
        &mut self,
        statement: impl Into<String>,
        justification: impl Into<String>,
        base_facts: Vec<ProvenanceVar>,
    ) {
        let step = self.proof_steps.len() + 1;
        self.proof_steps.push(ProofStep {
            step,
            statement: statement.into(),
            justification: justification.into(),
            base_facts,
        });
    }

    /// Generate a Rocq/Coq certificate skeleton for the proof.
    ///
    /// The generated certificate includes:
    /// - Theorem statement
    /// - Proof outline referencing the proof steps
    /// - Admitted subgoals (to be filled in by Rocq)
    pub fn generate_rocq_certificate(&mut self) {
        let mut cert = String::new();
        cert.push_str(&format!(
            "(* Auto-generated by MeTTaIL proof output layer *)\n"
        ));
        cert.push_str(&format!(
            "(* Property: {} *)\n",
            self.property
        ));
        cert.push_str(&format!(
            "(* Verdict: {} *)\n\n",
            self.verdict
        ));

        // Theorem statement.
        let theorem_name = self.property
            .replace(' ', "_")
            .replace(|c: char| !c.is_alphanumeric() && c != '_', "");
        cert.push_str(&format!(
            "Theorem {} :\n  (* {} *)\n  True. (* placeholder — replace with actual statement *)\n",
            theorem_name, self.property
        ));

        // Proof.
        cert.push_str("Proof.\n");
        for step in &self.proof_steps {
            cert.push_str(&format!(
                "  (* Step {}: {} [{}] *)\n",
                step.step, step.statement, step.justification
            ));
        }
        match self.verdict {
            Verdict::Verified => {
                cert.push_str("  trivial.\n");
            }
            Verdict::Violated => {
                cert.push_str("  (* Counterexample exists — property is false. *)\n");
                cert.push_str("  Admitted. (* The property does not hold. *)\n");
            }
            Verdict::Unknown => {
                cert.push_str("  Admitted. (* Analysis could not determine. *)\n");
            }
        }
        cert.push_str("Qed.\n");

        self.rocq_certificate = Some(cert);
    }

    /// Format as a human-readable proof trace.
    pub fn to_human_readable(&self) -> String {
        let mut out = String::new();
        out.push_str(&format!("Property: {}\n", self.property));
        out.push_str(&format!("Verdict:  {}\n", self.verdict));
        out.push_str(&format!("Provenance: {}\n", self.provenance));

        if !self.proof_steps.is_empty() {
            out.push_str("\nProof:\n");
            for step in &self.proof_steps {
                out.push_str(&format!("{}\n", step));
            }
        }

        if let Some(ref cert) = self.rocq_certificate {
            out.push_str(&format!("\nRocq Certificate:\n{}\n", cert));
        }

        out
    }
}

impl fmt::Display for ProofOutput {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.to_human_readable())
    }
}

