/// Pipeline bridge: generate proof certificates from all analysis results.
///
/// Consumes analysis results from all phases and produces proof certificates
/// for each verified property. Each certificate includes a verdict, provenance
/// polynomial, and human-readable proof steps.
///
/// Confluence and termination parameters require the `trs-analysis` feature;
/// safety checking is always available.
pub fn generate_certificates(
    #[cfg(feature = "trs-analysis")]
    confluence_result: Option<&crate::confluence::ConfluenceAnalysis>,
    #[cfg(feature = "trs-analysis")]
    termination_result: Option<&crate::termination::TerminationResult>,
    safety_result: Option<&crate::verify::SafetyResult<crate::automata::semiring::BooleanWeight>>,
) -> Vec<ProofOutput> {
    let mut outputs = Vec::new();

    // Confluence certificate
    #[cfg(feature = "trs-analysis")]
    if let Some(analysis) = confluence_result {
        if analysis.is_confluent {
            let mut proof = ProofOutput::new(
                "confluence",
                crate::verify::Verdict::Verified,
                ProvenanceWeight::var(ProvenanceVar::new("confluence_analysis")),
            );
            proof.add_step(
                format!("Checked {} critical pairs", analysis.critical_pairs.len()),
                "Knuth-Bendix criterion",
                vec![ProvenanceVar::new("critical_pair_enumeration")],
            );
            proof.add_step(
                format!(
                    "All {} critical pairs are joinable ({} non-joinable, {} unknown)",
                    analysis.critical_pairs.len(),
                    analysis.non_joinable_count,
                    analysis.unknown_count,
                ),
                "Joinability check",
                vec![ProvenanceVar::new("joinability_results")],
            );
            outputs.push(proof);
        } else {
            let mut proof = ProofOutput::new(
                "confluence",
                crate::verify::Verdict::Violated,
                ProvenanceWeight::var(ProvenanceVar::new("confluence_analysis")),
            );
            proof.add_step(
                format!(
                    "{} non-joinable critical pairs found",
                    analysis.non_joinable_count,
                ),
                "Knuth-Bendix criterion (failed)",
                vec![ProvenanceVar::new("non_joinable_pairs")],
            );
            outputs.push(proof);
        }
    }

    // Termination certificate
    #[cfg(feature = "trs-analysis")]
    if let Some(result) = termination_result {
        match result {
            crate::termination::TerminationResult::Terminating => {
                let mut proof = ProofOutput::new(
                    "termination",
                    crate::verify::Verdict::Verified,
                    ProvenanceWeight::var(ProvenanceVar::new("termination_analysis")),
                );
                proof.add_step(
                    "All SCCs have decreasing measures",
                    "Dependency pair method",
                    vec![ProvenanceVar::new("scc_ordering_witnesses")],
                );
                outputs.push(proof);
            }
            crate::termination::TerminationResult::PotentiallyNonTerminating {
                reason,
                problematic_sccs,
            } => {
                let mut proof = ProofOutput::new(
                    "termination",
                    crate::verify::Verdict::Unknown,
                    ProvenanceWeight::var(ProvenanceVar::new("termination_analysis")),
                );
                proof.add_step(
                    format!(
                        "Could not prove termination: {} (SCCs: {:?})",
                        reason, problematic_sccs,
                    ),
                    "Dependency pair method (inconclusive)",
                    vec![ProvenanceVar::new("problematic_sccs")],
                );
                outputs.push(proof);
            }
            crate::termination::TerminationResult::Unknown { reason } => {
                let mut proof = ProofOutput::new(
                    "termination",
                    crate::verify::Verdict::Unknown,
                    ProvenanceWeight::var(ProvenanceVar::new("termination_analysis")),
                );
                proof.add_step(
                    format!("Analysis inconclusive: {}", reason),
                    "Dependency pair method (inconclusive)",
                    vec![],
                );
                outputs.push(proof);
            }
        }
    }

    // Safety certificate
    if let Some(result) = safety_result {
        if result.safe {
            let mut proof = ProofOutput::new(
                "safety",
                crate::verify::Verdict::Verified,
                ProvenanceWeight::var(ProvenanceVar::new("safety_analysis")),
            );
            proof.add_step(
                "No bad state is reachable from the initial configuration",
                "Prestar analysis",
                vec![ProvenanceVar::new("prestar_fixpoint")],
            );
            outputs.push(proof);
        } else {
            let mut proof = ProofOutput::new(
                "safety",
                crate::verify::Verdict::Violated,
                ProvenanceWeight::var(ProvenanceVar::new("safety_analysis")),
            );
            proof.add_step(
                format!(
                    "Bad state reachable via witness trace of length {}",
                    result.witness_trace.len(),
                ),
                "Prestar analysis (counterexample found)",
                vec![ProvenanceVar::new("witness_trace")],
            );
            outputs.push(proof);
        }
    }

    outputs
}

